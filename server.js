'use strict';
(function(){

// *****************************************************************************
// Imports
// *****************************************************************************

const path    = require('path');
const express = require('express');

// *****************************************************************************
// Locals
// *****************************************************************************

const app          = express();
const _strPathDist = './dist';
const _strHost     = '0.0.0.0';
const _numPort     = 9090;

// *****************************************************************************
// Middleware
// *****************************************************************************

// setup
app.use(express.static(path.join(__dirname, _strPathDist)));

// routes
app.get('*', (req, res, next) =>
  res.sendFile(path.join(__dirname, _strPathDist, '/index.html')));

// *****************************************************************************
// Server
// *****************************************************************************

app.listen(_numPort, _strHost, () => {
  console.log(`Server listening on http://${_strHost}:${_numPort}`);
});

// *****************************************************************************

})();